package com.highfd.bean;
public class DicInfo implements java.io.Serializable {

	private static final long serialVersionUID = 1L;

	/**
	 * 主键(根据规则编号)
	 */
	private String dicCode;

	/**
	 * 中文名称
	 */
	private String dicCnName;

	/**
	 * 英文名称
	 */
	private String dicEnName;

	/**
	 * 顺序
	 */
	private String dicOrder;

	/**
	 * 数据字典类型编号
	 */
	private String dicTypeCode;

	public String getDicCode() {
		return dicCode;
	}

	public void setDicCode(String dicCode) {
		this.dicCode = dicCode;
	}

	public String getDicCnName() {
		return dicCnName;
	}

	public void setDicCnName(String dicCnName) {
		this.dicCnName = dicCnName;
	}

	public String getDicEnName() {
		return dicEnName;
	}

	public void setDicEnName(String dicEnName) {
		this.dicEnName = dicEnName;
	}

	public String getDicOrder() {
		return dicOrder;
	}

	public void setDicOrder(String dicOrder) {
		this.dicOrder = dicOrder;
	}

	public String getDicTypeCode() {
		return dicTypeCode;
	}

	public void setDicTypeCode(String dicTypeCode) {
		this.dicTypeCode = dicTypeCode;
	}

}
