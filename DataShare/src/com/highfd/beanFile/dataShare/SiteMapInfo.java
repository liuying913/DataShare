package com.highfd.beanFile.dataShare;

public class SiteMapInfo {
	
	private String siteNumber;
	private String siteName;
	private String siteLat;//纬度
	private String siteLng;//精度
	
	
	
	public int fileFlag;
	
	public String earthQuakeFlag;
	public String eLat;
	public String eLng;
	private String ephemNumber;//历元
	/**
	 * 应急事件  范围的方式
	 */
	public String crawtype;
	/**
	 * 应急事件  范围的大小
	 */
	public String crawrange ;
	/**
	 * 地震级别
	 */
	public String grade;
	
	public SiteMapInfo() {
		super();
	}
	public String getSiteNumber() {
		return siteNumber;
	}
	public void setSiteNumber(String siteNumber) {
		this.siteNumber = siteNumber;
	}
	public String getSiteName() {
		return siteName;
	}
	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}
	public String getSiteLat() {
		return siteLat;
	}
	public void setSiteLat(String siteLat) {
		this.siteLat = siteLat;
	}
	public String getSiteLng() {
		return siteLng;
	}
	public void setSiteLng(String siteLng) {
		this.siteLng = siteLng;
	}
	public int getFileFlag() {
		return fileFlag;
	}
	public void setFileFlag(int fileFlag) {
		this.fileFlag = fileFlag;
	}
	public String getEarthQuakeFlag() {
		return earthQuakeFlag;
	}
	public void setEarthQuakeFlag(String earthQuakeFlag) {
		this.earthQuakeFlag = earthQuakeFlag;
	}
	public String geteLat() {
		return eLat;
	}
	public void seteLat(String eLat) {
		this.eLat = eLat;
	}
	public String geteLng() {
		return eLng;
	}
	public void seteLng(String eLng) {
		this.eLng = eLng;
	}
	public String getCrawtype() {
		return crawtype;
	}
	public void setCrawtype(String crawtype) {
		this.crawtype = crawtype;
	}
	public String getCrawrange() {
		return crawrange;
	}
	public void setCrawrange(String crawrange) {
		this.crawrange = crawrange;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getEphemNumber() {
		return ephemNumber;
	}
	public void setEphemNumber(String ephemNumber) {
		this.ephemNumber = ephemNumber;
	}
}
